using System;
public class IfElseIf
{
    public static void Main()
    {
        int number = 500;
        if (number == 10)
        {
            Console.WriteLine("Number is 10");
        }
        else if (number == 20)
        {
            Console.WriteLine("Number is 20");
        }
        else if (number == 30)
        {
            Console.WriteLine("Number is 30");
        }
        else
        {
            Console.WriteLine("Number is not 10, 20 or 30");
        }
    }
} 
